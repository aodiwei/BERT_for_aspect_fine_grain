def tf_f1_score(y_true, y_pred, one_hot=False, num_class=2):
    f1s = [0, 0, 0]
    if one_hot:
        y_true = tf.one_hot(y_true, depth=num_class)
        y_pred = tf.one_hot(y_pred, depth=num_class)

    y_true = tf.cast(y_true, tf.float64)
    y_pred = tf.cast(y_pred, tf.float64)

    for i, axis in enumerate([None, 0]):
        TP = tf.count_nonzero(y_pred * y_true, axis=axis)
        FP = tf.count_nonzero(y_pred * (y_true - 1), axis=axis)
        FN = tf.count_nonzero((y_pred - 1) * y_true, axis=axis)

        precision = TP / (TP + FP)
        recall = TP / (TP + FN)
        if axis == 0:  # macro f1
            # start process nan class
            precision_nan = tf.is_nan(precision)
            recall_nan = tf.is_nan(recall)

            precision_nan_int = tf.cast(precision_nan, tf.int32)
            recall_nan_int = tf.cast(recall_nan, tf.int32)

            nanidx = precision_nan_int * recall_nan_int
            nanidx_bool = tf.cast(nanidx, tf.bool)

            non_nan_precision = tf.boolean_mask(precision, ~nanidx_bool)
            non_nan_recall = tf.boolean_mask(recall, ~nanidx_bool)

            f_mac = 2 * non_nan_precision * non_nan_recall / (non_nan_precision + non_nan_recall)
            f_mac = tf.where(tf.is_nan(f_mac), tf.zeros_like(f_mac), f_mac)
            macro = tf.reduce_mean(f_mac)
            # end process nan class

        f1 = 2 * precision * recall / (precision + recall)
        f1 = tf.where(tf.is_nan(f1), tf.zeros_like(f1), f1)
        f1s[i] = tf.reduce_mean(f1)

    weights = tf.reduce_sum(y_true, axis=0)
    weights /= tf.reduce_sum(weights)

    f1s[2] = tf.reduce_sum(f1 * weights)

    micro, _, weighted = f1s

    return micro, macro, weighted


